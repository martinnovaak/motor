static int evaluate(board& b) {
    int score = 0;
    int game_phase = 0;

    for (int piece = 0; piece < 5; piece++) {
        uint64_t bitboard = b.get_pieces(WHITE, piece);
        while(bitboard) {
            int square = pop_lsb(bitboard) ^ 56;
            score += piece_square_table[piece][square];
            game_phase += game_phase_inc[piece];
        }
    }

    for (int piece = 0; piece < 5; piece++) {
        uint64_t bitboard = b.get_pieces(BLACK, piece);
        while(bitboard) {
            int square = pop_lsb(bitboard);
            score -= piece_square_table[piece][square];
            game_phase += game_phase_inc[piece];
        }
    }

    if (popcount(b.get_pieces(WHITE, BISHOP)) == 2) {
        score += bishop_pair;
    }

    if (popcount(b.get_pieces(BLACK, BISHOP)) == 2) {
        score -= bishop_pair;
    }

    game_phase = std::min(game_phase, 24);
    score = (static_cast<int16_t>(score) * game_phase + static_cast<int16_t>(score >> 16) * (24 - game_phase)) / 24;

    return b.get_side() == WHITE ? score : -score;
}

